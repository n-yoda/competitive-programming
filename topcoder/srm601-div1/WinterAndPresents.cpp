class WinterAndPresents {
public:
	long long getNumber(vector <int> apple, vector <int> orange) {
		long long cnt = 0;
		long long amin, amax;
		int a, o;
		int size = apple.size();
		for (int i = 1; i <= 2000000; i++) {
			amin = 0;
			amax = 0;
			for (int j = 0; j < size; j ++) {
				if (apple[j] + orange[j] < i) {
					return cnt;
				} else {
					amin += i - min(orange[j], i);
					amax += min(apple[j], i);
				}
			}
			cnt += amax - amin + 1;
		}
		return cnt;
	}
};


